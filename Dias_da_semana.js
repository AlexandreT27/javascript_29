const semana = parseInt(prompt("Escolha um dia da semana"))

switch (semana) {
    case 1:
        alert("Domingo");

        break;

    case 2:
        alert("Segunda");

        break;

    case 3:
        alert("Terça");

        break;

    case 4:
        alert("Quarta");

        break;

    case 5:
        alert("SQuinta");

        break;

    case 6:
        alert("Sexta");

        break;

    case 7:
        alert("Sábado");

        break;

    default: alert("Por favor escolher um número válido")

}